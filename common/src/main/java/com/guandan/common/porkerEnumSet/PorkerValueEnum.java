	/**
	 * 根据 poker value获取花色
	 * @param nPorkerValue
	 * @return
	 */
	public static String getColorByPorkValue(int nPorkerValue){
		int nColorValue = PorkerValueEnum.getCardColorByPorkerValue(nPorkerValue);
		String strColorInfo = " ";
		switch(nColorValue){
		case CardColorEnum.CARD_COLOR_HEART:
			strColorInfo = "红桃";
			break;
		
		case CardColorEnum.CARD_COLOR_DIAMOND:
			strColorInfo = "方块";
			break;
			
		case CardColorEnum.CARD_COLOR_SPADE:
			strColorInfo = "黑桃";
			break;
			
		case CardColorEnum.CARD_COLOR_CLUB:
			strColorInfo = "草花";
			break;
			
		default:
			strColorInfo = "没有花色";
			break;
		}
		
		return strColorInfo;
	}
